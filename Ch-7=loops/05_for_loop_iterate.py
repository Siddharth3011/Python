# for loop for list
l = [1,"sidd", 20.5,50,True]
for i in l:
    print(i)

# for loop for tuple
t = (1,2,3,4,5)
for i in t:
    print(i)
# note: T = (1) is not a tuple, it is considered as a integer. To make it tuple we have to write T = (1,)

#for loop in string
s = "siddharth"
for i in s:
    print(i)

# for loop for dict
d = {
    "siddharth" : 100,
    "rohan" : 50
}
for i in d:
    print(i)

# for loop in sets
s= {1,2,3,5,5,6,7,8}
for i in s:
    print(i)